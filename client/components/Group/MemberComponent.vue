<!-- Show all member functionalities of a group -->

<template>
    <aside>
        <!-- <button v-if="editing" @click="submitEdit">
            ✅ Save changes
        </button>
        <button v-if="editing" @click="stopEditing">
            🚫 Discard changes
        </button>
        <button v-if="!editing" @click="startEditing">
            ✏️ Edit
        </button> -->
        <button v-if="!isMember" @click="joinGroup">
            <i class="fa fa-solid fa-arrow-right-to-bracket"></i> Join Group
        </button>
        <button v-if="isMember" @click="leaveGroup">
            <i class="fa fa-solid fa-arrow-right-from-bracket"></i> Leave Group
        </button>
    </aside>

</template>
  
<script>

export default {
    name: 'MemberComponent',
    props: {
        groupId: {
            type: String,
            required: true
        },
        role: {
            type: String,
            required: true
        }
    },
    data() {
        const role = this.role !== 'notJoined';
        return {
            isMember: role
        };
    },
    methods: {
        joinGroup() {
            /**
             * joins this group.
             */
            const params = {
                method: 'POST',
                callback: () => {
                    this.$store.commit('alert', {
                        message: 'Successfully joined group', status: 'success'
                    });
                }
            };
            
            // memberStatus = true;
            this.request(params);
        },
        leaveGroup() {
            /**
             * leaves this group.
             */
            const params = {
                method: 'DELETE',
                callback: () => {
                    this.$store.commit('alert', {
                        message: 'Successfully left group', status: 'success'
                    });
                }
            };

            // memberStatus = false;
            
            this.request(params);
        },
        async request(params) {
            /**
             * Submits a request to the owner's endpoint
             * @param params - Options for the request
             * @param params.body - Body for the request, if it exists
             * @param params.callback - Function to run if the the request succeeds
             */
            const options = {
                method: params.method, headers: { 'Content-Type': 'application/json' }
            };
            if (params.body) {
                options.body = params.body;
            }

            try {
                const r = await fetch(`/api/groups/${this.groupId}/member`, options);
                if (!r.ok) {
                    const res = await r.json();
                    throw new Error(res.error);
                }

                this.editing = false;
                this.$store.commit('refreshGroups');

                params.callback();
            } catch (e) {
                this.$set(this.alerts, e, 'error');
                setTimeout(() => this.$delete(this.alerts, e), 3000);
            }

        }
    }
};
</script>
  
